# Definition of dictionary
europe = {'spain':'madrid', 'france':'paris', 'germany':'berlin', 'norway':'oslo' }

# Print out the keys in europe
print(europe.keys())

# Print out value that belongs to key 'norway'
print(europe['norway'])

'''
O código acima demonstra como acessar as chaves e valores de um dicionário em Python. Utilizando o método `keys()`, podemos obter todas as chaves presentes no dicionário "europe", que representam os países. Além disso, ao acessar diretamente o valor associado a uma chave específica, como 'norway', podemos obter a capital correspondente, que é 'oslo'. Essa funcionalidade dos dicionários facilita a recuperação de informações de forma rápida e eficiente, tornando-os uma estrutura de dados poderosa para mapeamento de pares chave-valor.
'''