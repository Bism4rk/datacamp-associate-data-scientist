# Definition of dictionary
europe = {'spain':'madrid', 'france':'paris', 'germany':'berlin', 'norway':'oslo' }

# Add italy to europe
europe['italy'] = 'rome'

# Print out italy in europe
print('italy' in europe)

# Add poland to europe
europe['poland'] = 'warsaw'

# Print europe
print(europe)


'''
O código acima demonstra como adicionar novos pares chave-valor a um dicionário em Python. Ao adicionar a Itália com sua capital Roma e a Polônia com sua capital Varsóvia ao dicionário "europe", podemos facilmente expandir nosso conjunto de dados. Além disso, ao verificar se a Itália está presente no dicionário usando o operador `in`, podemos confirmar a existência de uma chave específica. Essa capacidade de adicionar e verificar elementos torna os dicionários uma estrutura de dados flexível e poderosa para armazenar informações relacionadas.
'''