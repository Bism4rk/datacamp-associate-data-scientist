# Initialize offset
offset = 8

# Code the while loop
while offset != 0:
    print('correcting...')
    offset = offset - 1
    print(offset)

'''
O código acima demonstra um loop while simples em Python. Ele começa com uma variável `offset` definida como 8 e, enquanto `offset` for diferente de 0, ele imprime "correcting...", diminui o valor de `offset` em 1 e imprime o novo valor de `offset`. O loop continua até que `offset` seja igual a 0.
'''