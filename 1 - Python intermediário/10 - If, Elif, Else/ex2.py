# Define variables
room = "kit"
area = 14.0

# if statement for room
if room == "kit" :
    print("looking around in the kitchen.")

# if statement for area
if area > 15:
    print("big place!")

'''
O código acima demonstra o uso de declarações if simples para verificar condições específicas. Se a variável 'room' for igual a "kit", ele imprimirá uma mensagem indicando que está olhando ao redor na cozinha. Se a variável 'area' for maior que 15, ele imprimirá uma mensagem indicando que o local é grande.
'''