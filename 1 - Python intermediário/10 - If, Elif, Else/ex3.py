# Define variables
room = "kit"
area = 14.0

# if-else construct for room
if room == "kit" :
    print("looking around in the kitchen.")
else :
    print("looking around elsewhere.")

# if-else construct for area
if area > 15 :
    print("big place!")
else:
    print("pretty small.")

'''
O código acima demonstra o uso de declarações if-else para verificar condições específicas. Se a variável 'room' for igual a "kit", ele imprimirá uma mensagem indicando que está olhando ao redor na cozinha; caso contrário, imprimirá que está olhando em outro lugar. Da mesma forma, se a variável 'area' for maior que 15, ele imprimirá que o local é grande; caso contrário, indicará que é bastante pequeno.
'''